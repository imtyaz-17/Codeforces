#include <bits/stdc++.h>
using namespace std;
void solution()
{
    // codeforces
    char c;
    cin >> c;
    if (c == 'c' || c == 'o' || c == 'd' || c == 'e' || c == 'f' || c == 'r' || c == 's')
        cout << "YES" << endl;
    else
        cout << "NO" << endl;

}
